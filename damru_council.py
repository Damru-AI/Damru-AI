def chat(prov: Dict[str, Any], messages: List[Dict[str, str]],
         max_tokens: Optional[int] = None, temperature: Optional[float] = None) -> str:
    mt = max_tokens or MAX_TOKENS
    tp = TEMPERATURE if temperature is None else temperature
    kind = prov["kind"]
    if kind == "openai":
        headers = {"Authorization": f"Bearer {prov['key']}", "Content-Type": "application/json"}
        payload = {"model": prov["model"], "messages": messages,
                   "max_tokens": mt, "temperature": tp}
        data = _http_post(prov["url"], headers, payload)
        choices = data.get("choices") or []
        if not choices:
            return ""
        return (choices[0].get("message", {}).get("content") or "")
    if kind == "cf":
        headers = {"Authorization": f"Bearer {prov['key']}", "Content-Type": "application/json"}
        payload = {"messages": messages, "max_tokens": mt, "temperature": tp}
        data = _http_post(prov["url"] + prov["model"], headers, payload)
        res = data.get("result", {}) or {}
        return res.get("response") or res.get("text") or ""
    if kind == "gemini":
        url = (f"https://generativelanguage.googleapis.com/v1beta/models/"
               f"{prov['model']}:generateContent?key={prov['key']}")
        parts = [{"text": f"{m['role'].upper()}: {m['content']}"} for m in messages]
        payload = {"contents": [{"parts": parts}],
                   "generationConfig": {"maxOutputTokens": mt, "temperature": tp}}
        data = _http_post(url, {"Content-Type": "application/json"}, payload)
        cands = data.get("candidates") or [{}]
        content = cands[0].get("content", {}) or {}
        return "".join(p.get("text", "") for p in content.get("parts", []))
    raise RuntimeError(f"unknown provider kind: {kind}")


def chat_safe(prov: Dict[str, Any], messages: List[Dict[str, str]], **kw) -> Optional[str]:
    try:
        return retry(chat, prov, messages, what=f"chat:{prov['name']}", **kw)
    except Exception as exc:
        log_error(f"chat:{prov['name']}", exc)
        return None


