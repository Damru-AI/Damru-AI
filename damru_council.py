def iter_problems() -> Iterable[Tuple[str, Dict[str, Any]]]:
    from huggingface_hub import hf_hub_download
    try:
        files = retry(hf_api().list_repo_files, repo_id=SRC_REPO,
                      repo_type="dataset", what="list:src")
    except Exception as exc:
        log_error("list:src", exc)
        return
    shards = sorted(f for f in files if f.endswith(".jsonl"))
    log.info("[src] %s: %d jsonl shards", SRC_REPO, len(shards))
    for fn in shards:
        try:
            local = retry(hf_hub_download, repo_id=SRC_REPO, repo_type="dataset",
                          filename=fn, token=HF_TOKEN, local_dir=DL_DIR, what=f"dl:{fn}")
        except Exception as exc:
            log_error(f"dl:{fn}", exc)
            continue
        try:
            with open(local, encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    try:
                        yield fn, json.loads(line)
                    except Exception:
                        continue
        finally:
            try:
                os.remove(local)
            except Exception:
                pass


# ---------------------------------------------------------------------------
# Shard writer
# ---------------------------------------------------------------------------
class ShardWriter:
    def __init__(self, state: Dict[str, Any], seen: set):
        self.state = state
        self.seen = seen
        self.buffers: Dict[str, List[str]] = {s: [] for s in SPLITS}
        self.dupes = 0

    def add(self, rec: Dict[str, Any], method: str, domain: str,
            provider: str, ms: Optional[int]) -> bool:
        split = rec.pop("split")
        rid = sha_id(split, json.dumps(rec, ensure_ascii=False, sort_keys=True))
        if rid in self.seen:
            self.dupes += 1
            return False
        self.seen.add(rid)
        out = {"id": rid, "kind": split, "domain": domain, "method": method,
               "provider": provider, "solve_ms": ms, "self_generated": True,
               "verified": method.startswith("verified"), **rec}
        self.buffers[split].append(json.dumps(out, ensure_ascii=False))
        if len(self.buffers[split]) >= SHARD_ROWS:
            self.flush(split)
        return True

    def flush(self, split: str) -> None:
        buf = self.buffers[split]
        if not buf:
            return
        idx = self.state["shard_index"][split]
        fname = f"{split}-{idx:05d}.jsonl"
        local = os.path.join(DATA_DIR, split, fname)
        atomic_write_text(local, "\n".join(buf) + "\n")
        self.state["shard_index"][split] = idx + 1
        self.state["rows_out"][split] = self.state["rows_out"].get(split, 0) + len(buf)
        self.state["total_out"] = self.state.get("total_out", 0) + len(buf)
        self.buffers[split] = []
        save_state(self.state)
        save_seen(self.seen)
        commit_all()  # throttled -> avoids 429
        log.info("[flush] %s shard %d (+%d) | total=%d", split, idx, len(buf), self.state["total_out"])

    def flush_all(self) -> None:
        for s in SPLITS:
            self.flush(s)


