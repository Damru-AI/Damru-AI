def council_solve(row: Dict[str, Any], providers: List[Dict[str, Any]],
                  judge: Optional[Dict[str, Any]], writer: ShardWriter) -> str:
    problem = row.get("problem")
    if not problem or len(problem) < 8:
        return "skip"
    domain = row.get("domain") or "other"
    ans = row.get("answer")
    tests = row.get("tests")
    judge_only = bool(row.get("judge_only"))

    pool = random.sample(providers, min(N_SOLVERS, len(providers)))
    candidates: List[Dict[str, Any]] = []
    for prov in pool:
        text, ms = solve(prov, problem, domain)
        if not text:
            continue
        passed = None
        if domain == "code" and tests:
            code = extract_code(text) or text
            passed = run_code_tests(code, tests)
        elif ans:
            cand_ans = extract_answer(text)
            if cand_ans is not None:
                passed = (norm_ans(cand_ans) == norm_ans(ans))
        candidates.append({"prov": prov["name"], "text": text, "ms": ms,
                           "passed": passed, "score": None})

    if not candidates:
        return "none"

    passing = [c for c in candidates if c["passed"] is True]
    failing = [c for c in candidates if c["passed"] is False]

    best = None
    method = None
    scored: List[Dict[str, Any]] = []
    if passing:
        # EFFICIENCY selector: shortest passing solution, tie -> fastest
        best = sorted(passing, key=lambda c: (len(c["text"]), c["ms"]))[0]
        method = "verified"
    elif judge_only or (not ans and not tests):
        for c in candidates:
            s = judge_score(judge, problem, c["text"])
            if s is not None:
                c["score"] = s
                scored.append(c)
        if scored:
            top = max(scored, key=lambda c: c["score"])
            if top["score"] >= JUDGE_MIN:
                best = top
                method = f"judge:{top['score']:.1f}"
    if best is None:
        return "reject"

    sol = best["text"].strip()
    writer.add({"split": "sft", "prompt": problem, "response": sol},
               method, domain, best["prov"], best.get("ms"))

    # DPO pair when we have a clear loser (verified winner vs failing, or judge gap)
    loser = None
    if failing:
        loser = failing[0]
    elif scored and len(scored) > 1:
        lo = min(scored, key=lambda c: c["score"])
        if lo is not best and (best["score"] or 0) - (lo["score"] or 0) >= 2:
            loser = lo
    if loser:
        writer.add({"split": "dpo", "prompt": problem,
                    "chosen": sol, "rejected": loser["text"].strip()},
                   method + "|dpo", domain, best["prov"], best.get("ms"))
    return "kept"


