_BOX = re.compile(r"\\boxed\{([^{}]*)\}")
_CODE = re.compile(r"```(?:python|py)?\s*\n(.*?)```", re.DOTALL)


def extract_answer(text: str) -> Optional[str]:
    if not text:
        return None
    m = _BOX.findall(text)
    if m:
        return m[-1].strip()
    if "####" in text:
        tail = text.split("####")[-1].strip()
        return tail.splitlines()[0].strip() if tail else None
    return None


def norm_ans(s: Optional[str]) -> Optional[str]:
    if s is None:
        return None
    s = s.strip().lower().rstrip(".")
    s = re.sub(r"[\s,$]", "", s)
    return s


def extract_code(text: str) -> Optional[str]:
    if not text:
        return None
    m = _CODE.search(text)
    return m.group(1) if m else None


def run_code_tests(solution_code: Optional[str], tests: Optional[str]) -> Optional[bool]:
    if not CODE_EXEC or not solution_code or not tests:
        return None
    if not isinstance(tests, str):
        tests = json.dumps(tests)
    script = solution_code + "\n\n" + tests + "\n"
    path = None
    try:
        with tempfile.NamedTemporaryFile("w", suffix=".py", delete=False, encoding="utf-8") as tf:
            tf.write(script)
            path = tf.name
        p = subprocess.run([sys.executable, path], capture_output=True,
                           timeout=CODE_TIMEOUT, text=True)
        return p.returncode == 0
    except Exception:
        return False
    finally:
        if path:
            try:
                os.remove(path)
            except Exception:
                pass


def judge_score(judge: Optional[Dict[str, Any]], problem: str, solution: str) -> Optional[float]:
    if not judge:
        return None
    prompt = ("You are a strict grader. Score the SOLUTION for the PROBLEM from 0 to 10 "
              "for correctness and quality. Reply with ONLY a number.\n\n"
              f"PROBLEM:\n{problem}\n\nSOLUTION:\n{solution}\n\nScore (0-10):")
    out = chat_safe(judge, [{"role": "user", "content": prompt}], max_tokens=8, temperature=0)
    if not out:
        return None
    m = re.search(r"(\d+(?:\.\d+)?)", out)
    return float(m.group(1)) if m else None


def solve(prov: Dict[str, Any], problem: str, domain: str) -> Tuple[Optional[str], int]:
    if domain == "code":
        sysmsg = ("You are an expert programmer. Return ONLY one Python code block "
                  "that solves the problem. No prose outside the code block.")
    elif domain == "math":
        sysmsg = ("You are an expert mathematician. Reason briefly, then give the "
                  "final answer inside \\boxed{}.")
    else:
        sysmsg = "You are a careful expert. Answer correctly and concisely."
    t0 = time.time()
    out = chat_safe(prov, [{"role": "system", "content": sysmsg},
                           {"role": "user", "content": problem}])
    ms = int((time.time() - t0) * 1000)
    return out, ms


